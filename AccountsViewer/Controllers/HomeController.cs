using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
using AccountsViewer.Models;

namespace AccountsViewer.Controllers
{
    //I added the backend project to this solution to account for future internal functionality
    //Largely leaving this controller untouched
